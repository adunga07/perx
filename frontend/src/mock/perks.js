export function getPerksByBusiness(businessId) {
  return perks.filter(p => p.businessId === businessId)
}

export function getPerksByCategory(category) {
  return perks.filter(p => p.category === category)
}

export function getPerkById(id) {
  return perks.find(p => p.id === id) ?? null
}
